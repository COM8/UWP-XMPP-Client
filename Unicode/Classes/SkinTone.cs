namespace NeoSmart.Unicode
{
    public enum SkinTone
    {
        /// <summary>
        /// The default to no skin color aka. yellow.
        /// </summary>
        NONE = 0,
        /// <summary>
        /// 🏻 light skin tone.
        /// </summary>
        LIGHT = 1,
        /// <summary>
        /// 🏼 medium-light skin tone.
        /// </summary>
        MEDIUM_LIGHT = 2,
        /// <summary>
        /// 🏽 medium skin tone.
        /// </summary>
        MEDIUM = 3,
        /// <summary>
        /// 🏾 medium-dark skin tone.
        /// </summary>
        MEDIUM_DARK = 4,
        /// <summary>
        /// 🏿 dark skin tone.
        /// </summary>
        DARK = 5
    }
}
