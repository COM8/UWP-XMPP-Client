namespace UWPX_UI_Context.Classes
{
    public enum ChatBackgroundMode
    {
        /// <summary>
        /// Show the splash screen image as chat background.
        /// </summary>
        SPLASH_IMAGE = 0,
        /// <summary>
        /// Show a custom image.
        /// </summary>
